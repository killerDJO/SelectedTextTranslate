#pragma once

/// <summary>
/// Represents available to application colors.
/// </summary>
enum class Colors
{
    /// <summary>
    /// The gray color (119, 199, 199).
    /// </summary>
    Gray = RGB(119, 119, 119),

    /// <summary>
    /// The black color (0, 0, 0).
    /// </summary>
    Black = RGB(0, 0, 0),

    /// <summary>
    /// The light gray color (170, 170, 170).
    /// </summary>
    LightGray = RGB(170, 170, 170)
};